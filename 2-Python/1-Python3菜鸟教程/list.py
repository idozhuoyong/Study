'''
list(列表)
常用方法：
list.append(x) 把一个元素添加到列表结尾
list.extend(L) 把一个列表中所有元素添加到列表结尾
list.insert(i, x) 在列表指定一个位置插入一个元素
list.remove(x) 移除列表中值为x的第一个元素
list.pop(i) 移除列表中指定位置元素，并返回，默认移除最后一个
list.clear() 清空列表
list.index(x) 返回列表中第一个值为x的元素的索引
list.count(x) 返回列表中x出现的次数
list.sort() 对列表中元素进行排序
list.reverse() 倒排列表中元素
list.copy() 返回列表的浅复制
'''